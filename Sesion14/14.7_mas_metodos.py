frase = "            Bienvenido a una nueva sesion del curso de python       "
print(frase)
print(frase.strip()) #Elimina los espacios en blanco al principio y al final del string
print(frase.rstrip()) #Elimina los espacios en blanco al final del string
print(frase.lstrip()) #Elimina los espacios en blanco al principio del string